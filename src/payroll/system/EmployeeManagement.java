    private void txt_ICKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_ICKeyReleased
        // TODO add your handling code here:
        String icText = txt_IC.getText().trim();

        if (!icText.isEmpty()) {
            if (icText.matches("^\\d{12}$")) {
                lab3.setText(null);  // Valid IC, clear error message
                
                int currentYear = cal.get(Calendar.YEAR);
                String ic = txt_IC.getText();
                char firstDigitChar = ic.charAt(0);
                char secondDigitChar = ic.charAt(1);
                int firstDigit = Character.getNumericValue(firstDigitChar);
                int secondDigit = Character.getNumericValue(secondDigitChar);

                int birthYear;
                if(firstDigit > 2)
                    birthYear = 1900 + (firstDigit*10) + secondDigit;
                else
                    birthYear = 2000 + (firstDigit*10) + secondDigit;

                int age = currentYear - birthYear;
                txt_age.setText(Integer.toString(age));
            } else {
                lab3.setText("Invalid IC");
                lab3.setForeground(Color.RED);
            }
        }else 
         { lab3.setText(null);
         }

    }//GEN-LAST:event_txt_ICKeyReleased

